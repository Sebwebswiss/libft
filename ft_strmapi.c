#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*res;
	int		i;

	i = 0;
	if (s == NULL || f == NULL)
		return (0);
	res = ft_calloc(sizeof(*res), ft_strlen(s) + 1);
	if (res == NULL)
		return (0);
	while (s[i])
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
